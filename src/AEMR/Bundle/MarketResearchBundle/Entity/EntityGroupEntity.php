<?php

namespace AEMR\Bundle\MarketResearchBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * EntityGroupEntity
 */
class EntityGroupEntity
{
    /**
     * @var integer
     */
    private $entitygroup_id;

    /**
     * @var integer
     */
    private $entity_id;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set entitygroup_id
     *
     * @param integer $entitygroupId
     * @return EntityGroupEntity
     */
    public function setEntitygroupId($entitygroupId)
    {
        $this->entitygroup_id = $entitygroupId;

        return $this;
    }

    /**
     * Get entitygroup_id
     *
     * @return integer 
     */
    public function getEntitygroupId()
    {
        return $this->entitygroup_id;
    }

    /**
     * Set entity_id
     *
     * @param integer $entityId
     * @return EntityGroupEntity
     */
    public function setEntityId($entityId)
    {
        $this->entity_id = $entityId;

        return $this;
    }

    /**
     * Get entity_id
     *
     * @return integer 
     */
    public function getEntityId()
    {
        return $this->entity_id;
    }

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }
}
